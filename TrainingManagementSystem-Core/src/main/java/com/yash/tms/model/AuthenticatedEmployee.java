package com.yash.tms.model;

/**
 * Authenticated Employee will be used to represent the authenticated employee after it logged in successfully.
 * @author prashant.chauhan
 *
 */
public class AuthenticatedEmployee {
	
	/**
	 * id will be used as a primary key in the employees table used for fetching
	 * employee.
	 */
	private int id;
	
	/**
	 * firstname will be used as First name of Employee.
	 */
	private String firstname;
	/**
	 * lastname will be used as Last name of Employee.
	 */
	private String lastname;
	/**
	 * email_id is a yash email id and it is unique.
	 */
	private String email_id;
	/**
	 * role will be used to represent the role of the Authenticated Employee
	 */
	private String role;
	/**
	 * status will be used to represent the status of the Authenticated Employee
	 */
	private String status;
	
	/**
	 * status will be used to represent the batch of the Authenticated Employee
	 */
	private String batch;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getEmail_id() {
		return email_id;
	}

	public void setEmail_id(String email_id) {
		this.email_id = email_id;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getBatch() {
		return batch;
	}

	public void setBatch(String batch) {
		this.batch = batch;
	}
}
