package com.yash.tms.util;

/**
 * This utility class is to initialize variables required to connect with ldap server
 * @author megha.dengre
 *
 */
public interface LDAPBasicInitializations {

	/**
	 * LDAP initial context factory is the name of the ldap vendor
	 */
	String LDAP_INITIAL_CONTEXT_FACTORY = "com.sun.jndi.ldap.LdapCtxFactory";
	
	/**
	 * It defines the security type
	 * security types can be as following
	 * 1. Simple
	 * 2. Ssl
	 * 3. Sasl
	 */
	String LDAP_SECURITY_AUTHENTICATION = "simple";
	
	/**
	 * It is a url of ldap provider (company specific)
	 */
	String LDAP_PROVIDER_URL = "ldap://inidradc01.yash.com/";
	
	/**
	 * It defines the domain name
	 */
	String LDAP_SEARCH_BASE = "DC=yash,DC=com";

}
