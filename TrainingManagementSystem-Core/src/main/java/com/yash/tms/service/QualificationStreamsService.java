package com.yash.tms.service;

import java.util.List;

import com.yash.tms.model.QualificationStreams;
/**
 * 'QualificationStreamsService' is a service containing all the methods and business logic
 *  required to perform operation on QualificationStreams object.
 * @author Sachin.jain
 *
 */
public interface QualificationStreamsService {
	/**
	 * 'getAllStreams()' method calls repository's method to fetch all streams from the database. 
	 * It returns List of all the QualificationStreams
	 * @return
	 */
	public List<QualificationStreams> getAllStreams();
	/**
	 * 'getCurrentStreams()' method calls repository's method to fetch single stream based on provided id from the database. 
	 * It returns list of QualificationStreams
	 * @return
	 */
	public List<QualificationStreams> getCurrentStreams(int employeeId);
}
